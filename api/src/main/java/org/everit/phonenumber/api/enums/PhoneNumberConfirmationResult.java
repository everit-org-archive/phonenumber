package org.everit.phonenumber.api.enums;

/**
 * Enumeration of the phone number confirmation result.
 * 
 */
public enum PhoneNumberConfirmationResult {

    /**
     * The phone confirmation result is success.
     */
    SUCCESS,

    /**
     * The phone confirmation result is rejected.
     */
    REJECTED,

    /**
     * The phone confirmation result is unknown phone number.
     */
    UNKNOWN_PHONENUM;
}
